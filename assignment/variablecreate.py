# Creating variables of different types

name = "Kinjal"          # String variable
age = 20                 # Integer variable
height = 5.4             # Float variable
is_student = True        # Boolean variable

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Checking data types
print("\nData Types:")
print(type(name))
print(type(age))
print(type(height))
print(type(is_student))


