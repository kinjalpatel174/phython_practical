#String Manipulation
#Write a Python program to demonstrate string slicing


# Define a string
text = "PythonProgramming"

# Different slicing examples
print("Original String:", text)

print("First 6 characters:", text[:6])        # From start to index 5
print("From index 6 to end:", text[6:])       # From index 6 to end
print("Between index 2 and 8:", text[2:8])    # From index 2 to 7
print("Every second character:", text[::2])   # Step value 2
print("Reverse string:", text[::-1])          # Reverse the string


