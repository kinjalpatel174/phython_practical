# Create a tuple with multiple data types
my_tuple = (10, "Python", 3.14, True, 'A')

# Print the tuple
print("Tuple:", my_tuple)

# Access elements
print("Integer:", my_tuple[0])
print("String:", my_tuple[1])
print("Float:", my_tuple[2])
print("Boolean:", my_tuple[3])
print("Character:", my_tuple[4])
