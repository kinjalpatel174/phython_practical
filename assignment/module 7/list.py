#Write a Python program to update a list using insert() and append

my_list = [1, 2, 3, 4]

# Update list using insert()
my_list.insert(2, 10)   # inserts 10 at index 2

# Update list using append()
my_list.append(20)      # adds 20 at the end

# Print updated list
print("Updated List:", my_list)
