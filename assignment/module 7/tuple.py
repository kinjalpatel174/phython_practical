# Write a Python program to create a tuple with multiple data types.

# Creating a tuple with different data types
my_tuple = (10, "Python", 3.14, True)

# Printing the tuple
print("Tuple:", my_tuple)

# Accessing elements
print("Integer value:", my_tuple[0])
print("String value:", my_tuple[1])
print("Float value:", my_tuple[2])
print("Boolean value:", my_tuple[3])
