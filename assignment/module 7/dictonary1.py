#Write a Python program to access values using dictionary keys.

# Create a dictionary
student = {
    "id": 101,
    "name": "Rohit",
    "age": 20,
    "course": "Python",
    "city": "Ahmedabad"
}

# Access values using keys
print("ID:", student["id"])
print("Name:", student["name"])
print("Age:", student["age"])
print("Course:", student["course"])
print("City:", student["city"])
